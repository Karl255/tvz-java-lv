package hr.java.vjezbe;

public class PrevisokaTemperaturaException extends Exception {
	public PrevisokaTemperaturaException() {
	}

	public PrevisokaTemperaturaException(String message) {
		super(message);
	}

	public PrevisokaTemperaturaException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrevisokaTemperaturaException(Throwable cause) {
		super(cause);
	}

	public PrevisokaTemperaturaException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
