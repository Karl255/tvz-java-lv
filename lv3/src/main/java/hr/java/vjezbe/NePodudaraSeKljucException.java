package hr.java.vjezbe;

public class NePodudaraSeKljucException extends Exception {
	public NePodudaraSeKljucException() {
	}

	public NePodudaraSeKljucException(String message) {
		super(message);
	}

	public NePodudaraSeKljucException(String message, Throwable cause) {
		super(message, cause);
	}

	public NePodudaraSeKljucException(Throwable cause) {
		super(cause);
	}

	public NePodudaraSeKljucException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
